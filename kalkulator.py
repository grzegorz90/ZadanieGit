
def suma(liczba1, liczba2):
    return liczba1 + liczba2

def roznica(liczba1, liczba2):
    return liczba1 - liczba2

def iloczyn(liczba1, liczba2):
    return liczba1 * liczba2

liczba1 = int(input())
liczba2 = int(input())
print("Suma: " + str(suma(liczba1,liczba2)))
print("Roznica: " + str(roznica(liczba1,liczba2)))
print("Iloczyn: " + str(iloczyn(liczba1,liczba2)))
